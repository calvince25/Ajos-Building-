-- Profiles Policies
CREATE POLICY "Public profiles are viewable by everyone" ON profiles FOR SELECT USING (true);
CREATE POLICY "Users can update their own profile" ON profiles FOR UPDATE USING (auth.uid() = id);
CREATE POLICY "Super Admins can manage all profiles" ON profiles FOR ALL USING (get_user_role() = 'super_admin'::user_role);

-- General Content Read Policies (Allow Public Select)
CREATE POLICY "Allow public read services" ON services FOR SELECT USING (status = 'published');
CREATE POLICY "Allow public read project_categories" ON project_categories FOR SELECT USING (true);
CREATE POLICY "Allow public read projects" ON projects FOR SELECT USING (status = 'published');
CREATE POLICY "Allow public read team_members" ON team_members FOR SELECT USING (status = 'published');
CREATE POLICY "Allow public read testimonials" ON testimonials FOR SELECT USING (status = 'published');
CREATE POLICY "Allow public read blog_categories" ON blog_categories FOR SELECT USING (true);
CREATE POLICY "Allow public read blog_posts" ON blog_posts FOR SELECT USING (status = 'published' OR get_user_role() IS NOT NULL);
CREATE POLICY "Allow public read careers" ON careers FOR SELECT USING (status = 'published' OR get_user_role() IS NOT NULL);
CREATE POLICY "Allow public read equipment" ON equipment FOR SELECT USING (status != 'archived');
CREATE POLICY "Allow public read website_settings" ON website_settings FOR SELECT USING (true);
CREATE POLICY "Allow public read seo_metadata" ON seo_metadata FOR SELECT USING (true);
CREATE POLICY "Allow public read faqs" ON faqs FOR SELECT USING (status = 'published');

