-- CREATE CUSTOM USER ROLE ENUM
CREATE TYPE user_role AS ENUM ('super_admin', 'admin', 'editor');

-- CREATE PROFILES TABLE (linked to auth.users)
CREATE TABLE profiles (
  id UUID REFERENCES auth.users(id) ON DELETE CASCADE PRIMARY KEY,
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  email TEXT NOT NULL,
  full_name TEXT,
  avatar_url TEXT,
  role user_role DEFAULT 'editor'::user_role NOT NULL
);

-- ENABLE ROW LEVEL SECURITY FOR PROFILES
ALTER TABLE profiles ENABLE ROW LEVEL SECURITY;

-- TRIGGER TO AUTOMATICALLY CREATE PROFILE ON SIGNUP
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS trigger AS $$
BEGIN
  INSERT INTO public.profiles (id, email, full_name, role)
  VALUES (
    new.id,
    new.email,
    COALESCE(new.raw_user_meta_data->>'full_name', ''),
    -- First user is super_admin, subsequent are editor by default
    CASE 
      WHEN NOT EXISTS (SELECT 1 FROM public.profiles) THEN 'super_admin'::user_role
      ELSE 'editor'::user_role
    END
  );
  RETURN new;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();

-- HELPER FUNCTIONS FOR RLS POLICIES
CREATE OR REPLACE FUNCTION get_user_role()
RETURNS user_role AS $$
  SELECT role FROM public.profiles WHERE id = auth.uid();
$$ LANGUAGE sql SECURITY DEFINER;

